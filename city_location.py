def get_coordinates(city_name: str) -> tuple:
    """
    根据城市名称获取坐标
    
    Args:
        city_name: 城市名称（支持别名）
    
    Returns:
        (经度, 纬度) 元组，如果未找到返回None
    """
    # 去除空格和常见后缀
    city = city_name.strip()
    city = city.replace("市", "").replace("地区", "").replace("州", "").replace("盟", "")
    
    # 精确匹配
    if city in CITY_COORDINATES:
        return CITY_COORDINATES[city]
    
    # 模糊匹配（检查是否包含关键词）
    for name, coords in CITY_COORDINATES.items():
        if city in name or name in city:
            return coords
    
    return None


def city_to_longitude(city_name: str) -> float:
    """
    获取城市的经度
    
    Args:
        city_name: 城市名称
    
    Returns:
        经度值，如果未找到返回None
    """
    coords = get_coordinates(city_name)
    if coords:
        return coords[0]
    return None


def city_to_latitude(city_name: str) -> float:
    """
    获取城市的纬度
    
    Args:
        city_name: 城市名称
    
    Returns:
        纬度值，如果未找到返回None
    """
    coords = get_coordinates(city_name)
    if coords:
        return coords[1]
    return None


def get_all_cities() -> list:
    """
    获取所有可用城市列表
    
    Returns:
        城市名称列表（去重后的正式名称）
    """
    # 提取正式城市名（排除别名）
    exclude_names = {"帝都", "魔都"}
    cities = [name for name in CITY_COORDINATES.keys() if name not in exclude_names]
    
    # 去重并排序
    unique_cities = sorted(list(set(cities)))
    return unique_cities


def search_city(keyword: str) -> list:
    """
    根据关键词搜索城市
    
    Args:
        keyword: 搜索关键词
    
    Returns:
        匹配的城市列表
    """
    results = []
    for city in get_all_cities():
        if keyword in city:
            results.append(city)
    return results


